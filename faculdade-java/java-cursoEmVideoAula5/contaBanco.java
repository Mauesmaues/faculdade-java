public class contaBanco {
    String conta;
    int numConta;
    String dono;
    float saldo;
    boolean status;

    contaBanco(String conta, int numConta, String dono, float saldo, boolean status){
        this.conta = conta;
        this.numConta = numConta;
        this.dono = dono;
        this.saldo = saldo;
        this.status = status;
    }


}
